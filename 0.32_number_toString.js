// Using the toString() function is the opposite of using the parseInt() function
// In order to ensure that a number or even a string is returned as a string, 
// you would use the toString() function.  If the number to be changed to a string
// is negative, the sign will carry over to the string.  
// If the radix is not specified, the default of 10 will be used.

// These will return the integers inside () to a string
console.log((17).toString());  // is '17'
console.log((17.2).toString()); // is '17.2'


